
CREATE TABLE PERSONA(
DNI	VARCHAR(15) PRIMARY KEY,
NOMBRE	 CHAR(25)  NOT NULL,
APELLIDO CHAR(30) NOT NULL,
CIUDAD   CHAR(25) NOT NULL,
DIRECCIONCALLE CHAR(50) NOT NULL,
DIRECCIONUM CHAR(5) NOT NULL,
TELEFONO   CHAR(15) NOT NULL,
FECHANAC   DATE NOT NULL,
VARON   cHAR (1) NOT NULL)engine=innodb;

CREATE TABLE DEPARTAMENTO(
IDDEPT CHAR(3) NOT NULL PRIMARY KEY,
NOMBRE CHAR(20),
PLANTA CHAR(6)) ENGINE=INNODB;

CREATE TABLE PROFESOR(
IDPROFESOR CHAR(4) NOT NULL, 
DNI CHAR(9) NOT NULL,
CODDEPT CHAR(10) ,
PRIMARY KEY( IDPROFESOR),
FOREIGN KEY (DNI) REFERENCES PERSONA(DNI),
FOREIGN KEY (CODDEPT) REFERENCES DEPARTAMENTO(IDDEPT))engine=innodb;

CREATE TABLE TITULACION(
IDTITULACION CHAR(6) NOT NULL PRIMARY KEY,
NOMBRE CHAR(20) NOT NULL)engine=innodb;

CREATE TABLE ASIGNATURA(
IDASIGNATURA	CHAR(6) PRIMARY KEY,
NOMBRE		CHAR(50) NOT NULL,
CREDITOS	NUMERIC NOT NULL,
CUATRIMESTRE	CHAR(1) NOT NULL,
COSTEBASICO	NUMERIC NOT NULL,
IDPROFESOR	CHAR(4) ,
IDTITULACION	CHAR(6) ,
CURSO		char(1) NOT NULL,
FOREIGN KEY (IDTITULACION) REFERENCES TITULACION (IDTITULACION),
FOREIGN KEY (IDPROFESOR) REFERENCES PROFESOR (IDPROFESOR))engine=innodb;


CREATE TABLE ALUMNO(
IDALUMNO CHAR(7) NOT NULL PRIMARY KEY,
DNI	CHAR(9) NOT NULL,
FOREIGN KEY (DNI) REFERENCES PERSONA(DNI))engine=innodb;

CREATE TABLE ALUMNO_ASIGNATURA(
IDALUMNO CHAR(7) NOT NULL PRIMARY KEY,
IDASIGNATURA CHAR(6) NOT NULL ,
NUMEROMATRICULA NUMERIC NOT NULL,
FOREIGN KEY (IDALUMNO) REFERENCES ALUMNO(IDALUMNO),
FOREIGN KEY (IDASIGNATURA) REFERENCES ASIGNATURA(IDASIGNATURA))engine=innodb;




