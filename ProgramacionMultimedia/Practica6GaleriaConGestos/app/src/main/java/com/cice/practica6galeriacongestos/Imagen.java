package com.cice.practica6galeriacongestos;

public class Imagen {
	
	private String titulo = "";
	private String url = "";
	
	
	public Imagen(String titulo, String url) {
		this.titulo = titulo;
		this.url = url;
	}


	public String getTitulo() {
		return titulo;
	}


	public String getUrl() {
		return url;
	}
	
	
	
	

}
